const clock = new THREE.Clock();
(function animate() {
  requestAnimationFrame(animate);
  const t = clock.getElapsedTime();

  controls.update();
  cloudMesh.rotation.y = t * 0.004;

  // Pulse markers
  markers.forEach(m => {
    const s = 1 + 0.35 * Math.sin(t * 2 + m.pulse.userData.pulsePhase);
    m.pulse.scale.setScalar(s);
    m.pulse.material.opacity = 0.3 * (1 - (s - 1) / 0.35);
  });

  // Country hover raycasting
  raycaster.setFromCamera(mouse, camera);
  const hits = raycaster.intersectObjects(countryMeshes);
  if (hits.length > 0) {
    const iso = hits[0].object.userData.iso;
    if (iso !== hoveredIso) {
      if (hoveredIso) setCountryHighlight(hoveredIso, false);
      hoveredIso = iso;
      setCountryHighlight(hoveredIso, true);
    }
    showTooltip(mouseScreen.x, mouseScreen.y, iso);
  } else {
    if (hoveredIso) { setCountryHighlight(hoveredIso, false); hoveredIso = null; }
    hideTooltip();
  }

  renderer.render(scene, camera);
})();

