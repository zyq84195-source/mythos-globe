const raycaster = new THREE.Raycaster();
const mouse     = new THREE.Vector2();
let mouseScreen = { x: 0, y: 0 };

function updateMouseNDC(clientX, clientY) {
  const rect = canvas.getBoundingClientRect();
  mouse.x =  ((clientX - rect.left) / rect.width)  * 2 - 1;
  mouse.y = -((clientY - rect.top)  / rect.height) * 2 + 1;
  mouseScreen.x = clientX;
  mouseScreen.y = clientY;
}

canvas.addEventListener('mousemove', e => { updateMouseNDC(e.clientX, e.clientY); });
canvas.addEventListener('mouseleave', () => {
  if (hoveredIso) { setCountryHighlight(hoveredIso, false); hoveredIso = null; }
  hideTooltip();
});

canvas.addEventListener('click', e => {
  updateMouseNDC(e.clientX, e.clientY);
  raycaster.setFromCamera(mouse, camera);

  // Check markers first
  const markerHits = raycaster.intersectObjects(markers.map(m => m.dot));
  if (markerHits.length > 0) {
    openModal(mythData[markerHits[0].object.userData.mythIndex]);
    return;
  }

  // Then check country click → open myth if available
  const countryHits = raycaster.intersectObjects(countryMeshes);
  if (countryHits.length > 0) {
    const iso = countryHits[0].object.userData.iso;
    const mythId = COUNTRY_TO_MYTH[iso];
    if (mythId) {
      const myth = mythData.find(m => m.id === mythId);
      if (myth) openModal(myth);
    }
  }
});

