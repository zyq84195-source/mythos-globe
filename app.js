function buildChaptersHTML(myth) {
  if (!myth.chapters || !myth.chapters.length) {
    // fallback for myths without chapters (shouldn't happen)
    return `<p class="chapter-content">${myth.summary}</p>`;
  }
  return myth.chapters.map(ch => `
    <div class="story-chapter">
      <h4 class="chapter-title">${ch.title}</h4>
      <p class="chapter-content">${ch.content}</p>
    </div>
  `).join('');
}

window.openModal = function(myth) {
  document.getElementById('modal-region').textContent = myth.region;
  document.getElementById('modal-title').textContent  = myth.title;

  // Hero image
  const heroImg = document.getElementById('modal-hero-image');
  const imgCaption = document.getElementById('modal-image-caption');
  if (heroImg) {
    if (myth.image && myth.image.url) {
      heroImg.src = myth.image.url;
      heroImg.style.display = '';
      if (imgCaption) imgCaption.textContent = myth.image.caption || '';
    } else {
      heroImg.style.display = 'none';
      if (imgCaption) imgCaption.textContent = '';
    }
  }

  // Chapters
  const chaptersEl = document.getElementById('modal-chapters');
  const bodyEl = document.getElementById('modal-body');
  if (chaptersEl) {
    chaptersEl.innerHTML = buildChaptersHTML(myth);
  } else if (bodyEl) {
    // Fallback: use modal-body with chapters HTML
    bodyEl.innerHTML = buildChaptersHTML(myth);
  }

  document.getElementById('audio-story-title').textContent = myth.title;
  document.getElementById('audio-progress-bar').style.width = '0%';

  if (currentSpeech) { speechSynthesis.cancel(); currentSpeech = null; clearInterval(speechInterval); }
  updateAudioIcon(false);

  document.getElementById('story-modal').classList.add('open');
  controls.autoRotate = false;
  if (window.lucide) lucide.createIcons();
};

