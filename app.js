// TopoJSON decoder
function topoToGeo(topology, o) {
  const arcs = topology.arcs;
  const scale = topology.transform ? topology.transform.scale : [1, 1];
  const translate = topology.transform ? topology.transform.translate : [0, 0];
  function decodeArc(idx) {
    const rev = idx < 0;
    const arc = arcs[rev ? ~idx : idx];
    let x = 0, y = 0;
    const pts = arc.map(([dx, dy]) => { x += dx; y += dy; return [x * scale[0] + translate[0], y * scale[1] + translate[1]]; });
    return rev ? pts.reverse() : pts;
  }
  function decodeRing(ring) { const c = []; ring.forEach(i => c.push(...decodeArc(i))); return c; }
  return {
    type: 'FeatureCollection',
    features: o.geometries.map(geom => {
      let coords;
      if (geom.type === 'Polygon') coords = geom.arcs.map(decodeRing);
      else if (geom.type === 'MultiPolygon') coords = geom.arcs.map(p => p.map(decodeRing));
      else coords = [];
      return { type:'Feature', id: geom.id, geometry:{ type: geom.type, coordinates: coords }, properties: geom.properties || {} };
    })
  };
}

function buildBorderLine(ring, opacity = 0.22, color = 0xffffff) {
  if (ring.length < 2) return null;
  const pts = ring.map(([lon, lat]) => latLonToVec3(lat, lon, 1.008));
  const geo  = new THREE.BufferGeometry().setFromPoints(pts);
  const mat  = new THREE.LineBasicMaterial({ color, transparent: true, opacity });
  return new THREE.Line(geo, mat);
}

function buildCountryFillMesh(rings) {
  // Build invisible collision mesh from the outer ring only
  const outerRing = rings[0];
  if (!outerRing || outerRing.length < 3) return null;
  const pts = outerRing.map(([lon, lat]) => latLonToVec3(lat, lon, 1.009));
  // Simple fan triangulation from centroid
  const cx = pts.reduce((s, p) => s + p.x, 0) / pts.length;
  const cy = pts.reduce((s, p) => s + p.y, 0) / pts.length;
  const cz = pts.reduce((s, p) => s + p.z, 0) / pts.length;
  const cLen = Math.sqrt(cx*cx + cy*cy + cz*cz);
  const center = new THREE.Vector3(cx/cLen * 1.009, cy/cLen * 1.009, cz/cLen * 1.009);
  const vertices = [];
  const indices  = [];
  vertices.push(center.x, center.y, center.z);
  pts.forEach(p => vertices.push(p.x, p.y, p.z));
  for (let i = 1; i < pts.length; i++) {
    indices.push(0, i, i === pts.length - 1 ? 1 : i + 1);
  }
  const geo = new THREE.BufferGeometry();
  geo.setAttribute('position', new THREE.Float32BufferAttribute(vertices, 3));
  geo.setIndex(indices);
  geo.computeVertexNormals();
  const mat = new THREE.MeshBasicMaterial({ transparent: true, opacity: 0, side: THREE.DoubleSide, depthWrite: false });
  return new THREE.Mesh(geo, mat);
}

