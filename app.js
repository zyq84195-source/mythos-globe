async function loadCountryBorders() {
  try {
    const resp = await fetch('./vendor/countries-110m.json');
    const topo = await resp.json();
    const geo  = topoToGeo(topo, topo.objects.countries);

    geo.features.forEach(f => {
      const geom = f.geometry;
      if (!geom) return;
      const iso = numericToA3(f.id);
      const polys = geom.type === 'MultiPolygon' ? geom.coordinates : [geom.coordinates];

      const isoKey = iso || ('_' + f.id);
      if (!countryHighlights[isoKey]) {
        countryHighlights[isoKey] = { fillMeshes: [], borderLines: [] };
      }

      polys.forEach(poly => {
        if (!poly[0]) return;

        // Border lines (default: faint white)
        const line = buildBorderLine(poly[0], 0.22, 0xffffff);
        if (line) { line.renderOrder = 3; bordersGroup.add(line); countryHighlights[isoKey].borderLines.push(line); }

        // Invisible raycasting mesh
        const rayMesh = buildCountryFillMesh(poly);
        if (rayMesh) {
          rayMesh.userData.iso = isoKey;
          rayMesh.renderOrder = 4;
          scene.add(rayMesh);
          countryMeshes.push(rayMesh);
        }

        // Highlight fill mesh (hidden by default)
        const hMesh = buildHighlightFillMesh(poly);
        if (hMesh) { hMesh.renderOrder = 5; scene.add(hMesh); countryHighlights[isoKey].fillMeshes.push(hMesh); }
      });
    });
  } catch(e) {
    console.warn('Border load failed', e);
  }
}
loadCountryBorders();

